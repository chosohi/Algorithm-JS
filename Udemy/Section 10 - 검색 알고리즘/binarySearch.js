function binarySearch(arr, value) {
  // add whatever parameters you deem necessary - good luck!
  let lt = 0;
  let rt = arr.length - 1;
  while (lt <= rt) {
    let mid = parseInt((lt + rt) / 2);
    if (arr[mid] < value) {
      lt = mid + 1;
    } else if (arr[mid] > value) {
      rt = mid - 1;
    } else if (arr[mid] === value) {
      return mid;
    }
  }

  return -1;
}

